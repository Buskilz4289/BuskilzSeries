// Seasons and episodes per series. Episode id format: {seriesId}-s{season}-e{episode}
function episode(seriesId, seasonNum, epNum, title) {
  return {
    id: `${seriesId}-s${seasonNum}-e${epNum}`,
    number: epNum,
    title,
  }
}

function season(seasonNum, episodes) {
  return { seasonNumber: seasonNum, episodes }
}

